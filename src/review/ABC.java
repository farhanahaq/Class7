package review;
//getter-setter to access private variable and method
public class ABC {
	
	private int x = 15;
	private int y = 25;
	
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}	
	
	public void setX(int x) {
		this.x =x;
		
	}

	public void setY(int y) {
		this.y=y;
	}
	
}
